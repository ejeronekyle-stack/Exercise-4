# mdas_operations.py
# #1 Arithmetic Operations (int, float, complex)

# Integer variables
a = 10
b = 3
print("Integer Operations:")
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)
print("Exponent:", a ** b)

# Float variables
x = 10.5
y = 2.5
print("\nFloat Operations:")
print("Addition:", x + y)
print("Subtraction:", x - y)
print("Multiplication:", x * y)
print("Division:", x / y)
print("Modulus:", x % y)
print("Exponent:", x ** y)

# Complex variables
m = 2 + 3j
n = 1 + 4j
print("\nComplex Operations:")
print("Addition:", m + n)
print("Subtraction:", m - n)
print("Multiplication:", m * n)
print("Division:", m / n)
print("Exponent:", m ** 2)
