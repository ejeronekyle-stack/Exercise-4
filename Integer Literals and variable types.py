# #3 Checking Variable Types

num_int = 100
num_float = 10.5
num_complex = 2 + 3j

print("\nVariable Types:")
print("Type of num_int:", type(num_int))
print("Type of num_float:", type(num_float))
print("Type of num_complex:", type(num_complex))
